""" 1. Qué operadores utiliza Python en los siguientes casos:
A. División Modular: %
B. Exponenciación: **
C. División que retorne entero: // """

num1=2
num2=4

print(f"A.Division modular: % \n{num1} % {num2} {num1%num2}")

print(f"B.Exponenciacion: ** \n{num1} ** {num2} {num1**num2} ")

print(f"C.Division que retorne entero: // \n {num1} // {num2} {num1//num2}")